//triple equal to (===)
// - true : It gives “true” only when the first value and data type are equal to the second value and data type.

console.log("5" === "5") // true
console.log(5 === 5) // true
console.log("cat" === "cat") // true

// - false : It gives “false” only when the first value and data type are equal to the second value and data type.

console.log(5 === "5") // false
console.log(5 === 6) // false
console.log("cat" === "Cat") // false
