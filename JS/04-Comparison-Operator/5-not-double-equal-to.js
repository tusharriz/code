//not double equal to (!==)
// - true : It gives “true” only when the first value or data type is not equal to the second value or data type.

console.log(5 !== "5") // true => number type of 5 is not equal to string type of 5.


// - false : It gives “false” only when the first value or data type is equal to the second value or data type.

console.log("5" !== "5") // false => it say's string type of 5 is not equal to string type of 5.
