//Note : Both pre increment ++i and post increment i++ are the same and both pre decrement --i and post decrement i-- are the same. 

//Increment:-

let x = 10;
x = x + 1; //increment by 1
console.log(x); //11

let y = 25;
y = y + 10; //increment by 10
console.log(y); //35

// Decrement:-

let a = 10;
a = a - 1; //decrement by 1
console.log(a); //9 

let b = 20;
b = b - 10; //decrement by 10
console.log(b); //10