// Mod of 6

function modOf6(num){
	//Write your code here
	let x = num % 6;
	if(x==0){
	    console.log("zero");
	}else if(x==1){
	    console.log("one");
	}else if(x==2){
	    console.log("two");
	}else if(x==3){
	    console.log("three");
	}else if(x==4){
	    console.log("four");
	}else if(x==5){
	    console.log("five");
	}
}
modOf6(7); // one