`You are given five numbers, stored in the variables with the following names
one, two, three, four, five
All the values are doubled, that is, they are multiplied by 2
Find the sum of the values stored in one, two, three, four, five , after the above operation are performed
`
let one = 1; two = 2; three = 3; four = 4; five = 5;

one = one * 2;
two = two * 2;
three = three * 2;
four = four * 2;
five = five * 2;

console.log( one + two + three + four + five ); // 30
